// ContractRule is one rule in a TFC template, each traceable to a 약관 article.
// Templates are human-ratified (G4 — LLM never generates rules).
type ContractRule struct {
	ID          string `json:"id"`
	Description string `json:"description"`
	Gate        string `json:"gate"`                    // "L1" | "L2"
	Provenance  string `json:"provenance"`              // "약관 제15조 제1항"
	BoundValue  string `json:"bound_value,omitempty"`   // instance-bound value after TCE
	Active      bool   `json:"active"`
}

// TFC (TPMN Format Contract) is the instance-bound contract for one document.
// Generated by TCE: category template + anchor A instance values.
type TFC struct {
	Category    string            `json:"category"`
	Version     string            `json:"version"`
	Rules       []ContractRule    `json:"rules"`
	BoundValues map[string]any    `json:"bound_values"`
	GeneratedAt string            `json:"generated_at"`
}

// ContractTemplate is the human-ratified category template stored in contractDB.
// Each rule carries 약관 조항 provenance — no rule may be LLM-authored.
type ContractTemplate struct {
	Category string         `json:"category"`
	Version  string         `json:"version"`
	Rules    []ContractRule `json:"rules"`
}

